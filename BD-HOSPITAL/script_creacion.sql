CREATE TABLE IF NOT EXISTS public.episodios_clinicos
(
    id_episodio integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    id_paciente integer NOT NULL,
    tipo_episodio character varying(30) COLLATE pg_catalog."default" NOT NULL,
    servicio character varying(50) COLLATE pg_catalog."default" NOT NULL,
    motivo_consulta text COLLATE pg_catalog."default",
    fecha_episodio date,
    CONSTRAINT episodios_clinicos_pkey PRIMARY KEY (id_episodio),
    CONSTRAINT fk_episodio_paciente FOREIGN KEY (id_paciente)
        REFERENCES public.pacientes (id_paciente) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE CASCADE
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.episodios_clinicos
    OWNER to postgres;


CREATE TABLE IF NOT EXISTS public.diagnosticos
(
    id_diagnostico integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    id_episodio integer NOT NULL,
    codigo_cie10 character varying(10) COLLATE pg_catalog."default" NOT NULL,
    descripcion_diagnostico character varying(200) COLLATE pg_catalog."default" NOT NULL,
    tipo_diagnostico character varying(20) COLLATE pg_catalog."default" NOT NULL,
    fecha_diagnostico date NOT NULL,
    CONSTRAINT diagnosticos_pkey PRIMARY KEY (id_diagnostico),
    CONSTRAINT fk_diagnostico_episodio FOREIGN KEY (id_episodio)
        REFERENCES public.episodios_clinicos (id_episodio) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE CASCADE,
    CONSTRAINT diagnosticos_tipo_diagnostico_check CHECK (tipo_diagnostico::text = ANY (ARRAY['principal'::character varying, 'secundario'::character varying]::text[]))
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.diagnosticos
    OWNER to postgres;


