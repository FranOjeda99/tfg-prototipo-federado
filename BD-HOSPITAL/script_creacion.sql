CREATE TABLE IF NOT EXISTS public.pacientes
(
    id_paciente integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    numero_historia_clinica character varying(20) COLLATE pg_catalog."default" NOT NULL,
    nombre character varying(50) COLLATE pg_catalog."default" NOT NULL,
    apellido_1 character varying(50) COLLATE pg_catalog."default" NOT NULL,
    apellido_2 character varying(50) COLLATE pg_catalog."default",
    fecha_nacimiento date NOT NULL,
    sexo character(1) COLLATE pg_catalog."default" NOT NULL,
    id_zona integer NOT NULL,
    CONSTRAINT pacientes_pkey PRIMARY KEY (id_paciente),
    CONSTRAINT pacientes_numero_historia_clinica_key UNIQUE (numero_historia_clinica),
    CONSTRAINT fk_paciente_zona FOREIGN KEY (id_zona)
        REFERENCES public.zonas_residencia (id_zona) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE RESTRICT,
    CONSTRAINT pacientes_sexo_check CHECK (sexo = ANY (ARRAY['M'::bpchar, 'F'::bpchar]))
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.pacientes
    OWNER to postgres;


