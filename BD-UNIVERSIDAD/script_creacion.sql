CREATE TABLE IF NOT EXISTS public.investigadores
(
    id_investigador integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    nombre character varying(50) COLLATE pg_catalog."default" NOT NULL,
    apellido_1 character varying(50) COLLATE pg_catalog."default" NOT NULL,
    apellido_2 character varying(50) COLLATE pg_catalog."default",
    email_institucional character varying(100) COLLATE pg_catalog."default" NOT NULL,
    departamento character varying(100) COLLATE pg_catalog."default" NOT NULL,
    categoria character varying(50) COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT investigadores_pkey PRIMARY KEY (id_investigador),
    CONSTRAINT uq_investigadores_email UNIQUE (email_institucional)
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.investigadores
    OWNER to postgres;


CREATE TABLE IF NOT EXISTS public.admins
(
    id_admin integer NOT NULL DEFAULT nextval('admins_id_admin_seq'::regclass),
    username character varying(100) COLLATE pg_catalog."default" NOT NULL,
    password_hash character varying(255) COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT admins_pkey PRIMARY KEY (id_admin),
    CONSTRAINT admins_username_key UNIQUE (username)
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.admins
    OWNER to postgres;
-- Index: ix_admins_id_admin

